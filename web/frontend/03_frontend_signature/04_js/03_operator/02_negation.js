
// 부정 Negation

console.log(!true);
console.log(!false);

console.log(!0);
console.log(!!0);
console.log(!!!0);
console.log(!null);
console.log(!undefined);
console.log(!NaN);
console.log(!'');

console.log(!{});
console.log(![]);


// 비교 Comparison

const a = 1;
const b = 3;

// 동등(형 변환!)
console.log(a == b);

// 부등 (형 변환!)
console.log(a != b);

// 일치
console.log(a === b);

// 불일치
console.log(a !== b);

// 큼
console.log(a > b);

// 크거나 같음
console.log(a >= b);

// 작음
console.log(a < b);
